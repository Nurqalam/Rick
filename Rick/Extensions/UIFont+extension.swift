import UIKit

extension UIFont {

    static func specialFont22() -> UIFont? {
        return UIFont.init(name: "HelveticaNeue-bold", size: 22)
    }

    static func specialFont18() -> UIFont? {
        return UIFont.init(name: "HelveticaNeue-bold", size: 18)
    }
}
